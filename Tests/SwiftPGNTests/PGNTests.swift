import XCTest
@testable import SwiftPGN

final class PGNTests: XCTestCase {

    
    func test_PGN_moveE2E4_parsedCorrectly() {
        let pgnString = "1.e2 e4"
        let expected = PGN(counterparts: [WhiteMove("e2")!, BlackMove("e4")!])
        let sut = PGN(pgnString)
        XCTAssertEqual(sut, expected)
    }


    static var allTests = [
        ("test_PGN_onlyOneMove_parsedCorrectly", test_PGN_moveE2E4_parsedCorrectly),
    ]
}
